-- 012: Multi-company accounting foundation (Phase 1 of the analytic port).
-- Company 1 = the Drone Plant, holding all existing books and the physical
-- floor. Companies scope the ACCOUNTING layer only (chart of accounts, cost
-- ledger, customers, vendors, sales, purchasing, intake); the physical
-- simulation (facilities, zones, production, inventory, standards) stays
-- company-1-implicit. Idempotent; the same statements live in schema.sql.
--
-- Immutability note: cost_lines/cost_entries carry UPDATE-blocking triggers.
-- ADD COLUMN ... DEFAULT 1 backfills existing rows as a DDL rewrite WITHOUT
-- firing row-level UPDATE triggers, so no per-row UPDATE is needed. The
-- posting engine sets company_id on INSERT (triggers only block UPDATE).

CREATE TABLE IF NOT EXISTS companies (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  -- Short label for headers/switcher; display only, id stays the key.
  code TEXT UNIQUE,
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
