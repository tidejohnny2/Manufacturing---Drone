-- ===== Chart of accounts: scope per company =====
-- gl_accounts PK moves from (account_no) to (company_id, account_no); the
-- cost_lines FK re-points to the composite key.
ALTER TABLE cost_lines DROP CONSTRAINT IF EXISTS cost_lines_account_no_fkey;

ALTER TABLE gl_accounts ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
DO $$
BEGIN
  -- Swap the single-column PK for the composite one (once).
  IF EXISTS (
    SELECT 1 FROM information_schema.table_constraints
    WHERE table_name = 'gl_accounts' AND constraint_type = 'PRIMARY KEY'
      AND constraint_name = 'gl_accounts_pkey'
  ) AND NOT EXISTS (
    SELECT 1 FROM information_schema.key_column_usage
    WHERE constraint_name = 'gl_accounts_pkey' AND column_name = 'company_id'
  ) THEN
    ALTER TABLE gl_accounts DROP CONSTRAINT gl_accounts_pkey;
    ALTER TABLE gl_accounts ADD CONSTRAINT gl_accounts_pkey PRIMARY KEY (company_id, account_no);
  END IF;
END $$;

-- ===== Cost ledger: scope entries and lines =====
ALTER TABLE cost_entries ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE cost_lines ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
-- Re-point the line->account FK to the composite key (idempotent).
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.table_constraints
    WHERE table_name = 'cost_lines' AND constraint_name = 'cost_lines_company_account_fkey'
  ) THEN
    ALTER TABLE cost_lines
      ADD CONSTRAINT cost_lines_company_account_fkey
      FOREIGN KEY (company_id, account_no) REFERENCES gl_accounts(company_id, account_no);
  END IF;
END $$;
CREATE INDEX IF NOT EXISTS idx_cost_entries_company ON cost_entries(company_id);
CREATE INDEX IF NOT EXISTS idx_cost_lines_company ON cost_lines(company_id);

-- ===== Subledgers and order-to-cash / procure-to-pay: scope per company =====
ALTER TABLE customers ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE vendors ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE sales_orders ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE invoices ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE purchase_orders ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE price_list ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);
ALTER TABLE inbound_emails ADD COLUMN IF NOT EXISTS company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id);

-- customers/vendors unique-by-name becomes unique per company.
ALTER TABLE customers DROP CONSTRAINT IF EXISTS customers_name_key;
ALTER TABLE vendors DROP CONSTRAINT IF EXISTS vendors_name_key;
CREATE UNIQUE INDEX IF NOT EXISTS customers_company_name_key ON customers(company_id, name);
CREATE UNIQUE INDEX IF NOT EXISTS vendors_company_name_key ON vendors(company_id, name);
-- Account codes are unique per company too (C-1001+ can restart per company).
ALTER TABLE customers DROP CONSTRAINT IF EXISTS customers_account_code_key;
ALTER TABLE vendors DROP CONSTRAINT IF EXISTS vendors_account_code_key;
CREATE UNIQUE INDEX IF NOT EXISTS customers_company_code_key ON customers(company_id, account_code);
CREATE UNIQUE INDEX IF NOT EXISTS vendors_company_code_key ON vendors(company_id, account_code);

CREATE INDEX IF NOT EXISTS idx_customers_company ON customers(company_id);
CREATE INDEX IF NOT EXISTS idx_vendors_company ON vendors(company_id);
CREATE INDEX IF NOT EXISTS idx_sales_orders_company ON sales_orders(company_id);
CREATE INDEX IF NOT EXISTS idx_purchase_orders_company ON purchase_orders(company_id);

