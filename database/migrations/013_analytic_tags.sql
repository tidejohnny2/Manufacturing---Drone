FROM (VALUES ('Assembly Line'), ('Case Line')) AS v(name)
JOIN tag_groups g ON g.company_id = 1 AND g.name = 'Cost Center'
ON CONFLICT DO NOTHING;

-- Customer / Vendor tags mirror the subledgers (kept in sync at runtime too).
INSERT INTO account_tags (company_id, tag_group_id, name, reference)
SELECT 1, g.id, c.name, c.account_code
FROM customers c
JOIN tag_groups g ON g.company_id = 1 AND g.name = 'Customer'
WHERE c.company_id = 1 AND c.account_code IS NOT NULL
ON CONFLICT DO NOTHING;

INSERT INTO account_tags (company_id, tag_group_id, name, reference)
SELECT 1, g.id, ven.name, ven.account_code
FROM vendors ven
JOIN tag_groups g ON g.company_id = 1 AND g.name = 'Vendor'
WHERE ven.company_id = 1 AND ven.account_code IS NOT NULL
ON CONFLICT DO NOTHING;
