-- 013: Analytic tags (Phase 2 of the analytic port). A report-only
-- dimensional layer over the cost ledger: tag groups (dimensions), account
-- tags (values), and per-line tag distributions. INTEGRITY RULE: no trial
-- balance or control query joins these tables — the books are byte-identical
-- with or without tag data. Idempotent; mirrored in schema.sql.

CREATE TABLE IF NOT EXISTS tag_groups (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id),
  name TEXT NOT NULL,
  color TEXT,
  UNIQUE (company_id, name)
);

-- An account tag is a value inside a group. `reference` links a tag to the
-- source entity (SKU, customer/vendor account code) for auto-tagging.
CREATE TABLE IF NOT EXISTS account_tags (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id INTEGER NOT NULL DEFAULT 1 REFERENCES companies(id),
  tag_group_id INTEGER NOT NULL REFERENCES tag_groups(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  reference TEXT,
  UNIQUE (tag_group_id, name)
);
CREATE INDEX IF NOT EXISTS idx_account_tags_group ON account_tags(tag_group_id);

-- A cost line's share of a dimension. Report-only; a separate table so
-- writing tags never touches the immutable ledger.
CREATE TABLE IF NOT EXISTS tag_distributions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cost_line_id INTEGER NOT NULL REFERENCES cost_lines(id) ON DELETE CASCADE,
  account_tag_id INTEGER NOT NULL REFERENCES account_tags(id) ON DELETE CASCADE,
  percentage NUMERIC(7, 4) NOT NULL CHECK (percentage > 0 AND percentage <= 100),
  UNIQUE (cost_line_id, account_tag_id)
);
CREATE INDEX IF NOT EXISTS idx_tag_dist_line ON tag_distributions(cost_line_id);
CREATE INDEX IF NOT EXISTS idx_tag_dist_tag ON tag_distributions(account_tag_id);

