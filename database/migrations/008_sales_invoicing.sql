CREATE TABLE IF NOT EXISTS sales_orders (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  so_no TEXT NOT NULL UNIQUE,
  customer_id INTEGER NOT NULL REFERENCES customers(id),
  status TEXT NOT NULL DEFAULT 'open' CHECK (status IN ('open', 'invoiced', 'cancelled')),
  requested_date DATE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS sales_order_lines (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sales_order_id INTEGER NOT NULL REFERENCES sales_orders(id) ON DELETE CASCADE,
  sku TEXT NOT NULL,
  quantity INTEGER NOT NULL CHECK (quantity > 0),
  unit_price NUMERIC(12, 2) NOT NULL CHECK (unit_price >= 0)
);

CREATE TABLE IF NOT EXISTS invoices (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  invoice_no TEXT NOT NULL UNIQUE,
  sales_order_id INTEGER NOT NULL UNIQUE REFERENCES sales_orders(id),
  customer_id INTEGER NOT NULL REFERENCES customers(id),
  subtotal NUMERIC(12, 2) NOT NULL,
  cogs NUMERIC(12, 2) NOT NULL,
  invoiced_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

