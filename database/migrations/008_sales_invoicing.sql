-- Sales: customers, price list, sales orders, and invoicing, plus the
-- order-to-cash GL accounts. For databases created before this feature;
-- fresh databases get everything from schema.sql. Safe to re-run.

CREATE TABLE IF NOT EXISTS customers (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  contact TEXT NOT NULL DEFAULT '',
  terms TEXT NOT NULL DEFAULT 'Net 30',
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
